using System;
using System.Collections.Generic;
using System.Text;

namespace Lab1
{
    public class Addition
    {
        public static int Add(int num1, int num2)
        {
            return num1 + num2;
        }

        public static double Add(double num1, double num2, double num3)
        {
            return num1 + num2 + num3;
        }
    }
}
